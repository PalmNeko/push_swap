int	ft_isalnum(int c)
{
	if ('0' <= c && c <= '9')
		return (!0);
	else if ('A' <= c && c <= 'Z')
		return (!0);
	else if ('a' <= c && c <= 'z')
		return (!0);
	else
		return (0);
}
