/**
 * If c is '\t', '\n', '\v', '\f', '\r' or ' ', return non-zero.
 * else, return zero.
 */
int	ft_isspace(char c)
{
	if (('\t' <= c && c <= '\r') || c == ' ')
		return (1);
	else
		return (0);
}
